public class OutputStatementExercise {
	
	public static void main(String[] args) {

		System.out.println("1)");
		System.out.println();
		System.out.println();
		System.out.println("");
		System.out.print("FUN");
		System.out.print("");
		System.out.println("stuff");

		System.out.println("\n\n2)");
		System.out.print("Wuzzup\nDoc");
		System.out.println("\t??");

		System.out.println("\n\n3)");
		System.out.println("Hello." + "How are");
		System.out.println("you" + "today" + "?");

		System.out.println("\n\n4)");
		System.out.println("4 + 3");
		System.out.println(4 + 3);
		System.out.println("38 - 9 = " + (38 - 9));
		System.out.println("3 plus 3 is " + 3 + 3);
		System.out.println("3 plus 3 is " + (3 + 3));

		System.out.println("\n\n5)");
		System.out.println(18 - 7);
		System.out.println(12 * 3);
		System.out.println((4 + 5) * (13 - 9));
		System.out.println(7 - 4 * 3 + 1);
		System.out.println(24 / 6);
		System.out.println((32 - 6 * 2) / 5);
	}
}
